def spotTheDifference(s: str, t: str) -> str:
  # Time: O(n+m)
  # Space: O(1)
  sumASC_S = 0
  sumASC_T = 0
  
  for c in s:
    sumASC_S += ord(c)
  
  for c in t:
    sumASC_T += ord(c)
  
  return "" if sumASC_T-sumASC_S == 0 else chr(sumASC_T-sumASC_S)

